/**
 * サイドバーシステムの型定義
 */

/**
 * サイドバーアイテムのインターフェース
 */
export interface SidebarItem {
	/** 一意のID */
	id: string
	/** 表示ラベル */
	label: string
	/** アイコン名 */
	icon?: string
	/** リンク先URL */
	href?: string
	/** クリック時のアクション */
	action?: () => void | Promise<void>
	/** 子要素（最大3階層） */
	children?: SidebarItem[]
	/** グループ名（セクション分け用） */
	group?: string
	/** 表示順序（昇順） */
	order?: number
	/** バッジ表示（通知数など） */
	badge?: string | number
	/** 無効化フラグ */
	disabled?: boolean
	/** 表示条件（認証など） */
	visible?: boolean | (() => boolean)
	/** 必要なロール（admin、super_adminなど） */
	requiredRole?: string
	/** メタデータ（拡張用） */
	meta?: Record<string, unknown>
	/**
	 * アクティブ判定用URLプレフィックス
	 * 現在のパスがいずれかで startsWith すればアクティブとみなす
	 */
	activeMatch?: string[]
}

/**
 * サイドバーグループのインターフェース
 */
export interface SidebarGroup {
	/** グループID */
	id: string
	/** グループラベル */
	label: string
	/** 表示順序 */
	order?: number
	/** 折りたたみ可能か */
	collapsible?: boolean
	/** デフォルトで展開されているか */
	defaultExpanded?: boolean
	/** 必要なロール（admin、super_adminなど） */
	requiredRole?: string
}

/**
 * サイドバーの表示モード
 */
export type SidebarMode = 'expanded' | 'hover'

/**
 * サイドバーの状態
 */
export interface SidebarState {
	/** 現在のモード */
	mode: SidebarMode
	/** 展開されているアイテムのID配列 */
	expandedItems: string[]
	/** アクティブなアイテムのID */
	activeItemId?: string
	/** グループの展開状態 */
	expandedGroups: string[]
	/** サイドバーが開いているか（モバイル用） */
	isOpen: boolean
}

/**
 * サイドバー設定
 */
export interface SidebarConfig {
	/** デフォルトモード */
	defaultMode?: SidebarMode
	/** localStorage のキー名 */
	storageKey?: string
	/** アイコンサイズ */
	iconSize?: 'sm' | 'md' | 'lg'
	/** トランジション時間（ms） */
	transitionDuration?: number
	/** ホバーモード時の遅延（ms） */
	hoverDelay?: number
	/** 最大階層数 */
	maxDepth?: number
	/** モバイルブレークポイント */
	mobileBreakpoint?: number
}

